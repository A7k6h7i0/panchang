export const getCurrentRashi = (date) => {
  const dayOfYear = Math.floor((date - new Date(date.getFullYear(), 0, 0)) / 1000 / 60 / 60 / 24);
  const rashiIndex = Math.floor((dayOfYear % 360) / 30);
  return RASHIS[rashiIndex].id;
};

